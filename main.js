//  switch...case — это конструкция в JavaScript, используемая для выбора и выполнения одного из нескольких блоков кода на основе значения выражения. Она позволяет заменить множество if...else конструкций, если вы проверяете одно и то же значение на равенство нескольким возможным вариантам.
//
//  Вот синтаксис:
//
//  switch (выражение) {
//  case значение1:
//     Код, который выполнится, если выражение === значение1
//    break;
//  case значение2:
//    Код, который выполнится, если выражение === значение2
//    break;
//   Дополнительные case
//  default:
//     Код, который выполнится, если ни один из case не совпал
//    break;
//  }
//
//  • выражение — это переменная или выражение, значение которого будет проверяться.
//  • case значениеN: — каждый case проверяет, равно ли выражение указанному значению.
//  • break — используется, чтобы остановить выполнение последующих case, если совпадение уже найдено.
//  • default — выполняется, если ни одно из значений case не совпало с выражением. Необязателен.
//
//  Пример использования:
//
//  let fruit = "apple";
// let fruitt ="banana"
